package enums;

/**
 * Enumeración que representa los diferentes tipos de residentes. Cada valor de
 * esta enumeración corresponde a una categoría de residente en el sistema.
 */
public enum TipoResidente {

    /**
     * Residente de nuevo ingreso, que acaba de ingresar al sistema o
     * institución.
     */
    NUEVO_INGRESO,
    /**
     * Residente deportista, que participa activamente en actividades
     * deportivas.
     */
    DEPORTISTA,
    /**
     * Residente extranjero, que proviene de otro país.
     */
    EXTRANJERO
}
