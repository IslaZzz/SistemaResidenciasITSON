package enums;

/**
 * Enumeración que representa los diferentes tipos de puestos en una
 * organización o institución. Cada valor de esta enumeración corresponde a un
 * tipo de puesto específico.
 */
public enum Puesto {

    /**
     * Puesto de limpieza, encargado de mantener las instalaciones limpias.
     */
    LIMPIEZA,
    /**
     * Puesto de mantenimiento, encargado del mantenimiento y reparación de las
     * instalaciones.
     */
    MANTENIMIENTO,
    /**
     * Puesto administrativo, encargado de tareas administrativas y de gestión.
     */
    ADMINISTRATIVO
}
